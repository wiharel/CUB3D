#include "../../includes/libft.h"

void	ft_bzero(void *str, int n)
{
	int				i;
	unsigned char	*tmp;

	i = 0;
	tmp = str;
	while (i < n)
	{
		tmp[i] = '\0';
		i++;
	}
}
